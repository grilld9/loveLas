package grillid9.laslib;

public class WellInfo {

    private Float start;
    private Float stop;

    public Float getStart() {
        return start;
    }

    public void setStart(Float start) {
        this.start = start;
    }

    public Float getStop() {
        return stop;
    }

    public void setStop(Float stop) {
        this.stop = stop;
    }

    public Float getStep() {
        return step;
    }

    public void setStep(Float step) {
        this.step = step;
    }

    private Float step;
}
